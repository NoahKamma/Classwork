package MakeYourChoiceGame;

public class TextTheMan67 {

	public static String TheManWelcome()
	{
		return "Good evening sir.\n"
				+ "I heard that you were running low on funds.\n"
				+ "Wanna get a quick buck?\n"
				+ "";
	}
	
	public static String signThis()
	{
		return "Alright, first I need to sign you this totally non-binding waiver.\n"
				+ "For legal reasons.\n";
	}
	
	public static String askOne()
	{
		return "Congratulations, you are now a temporary dispatcher for ‘The Company’.\n"
				+ "Your job is to complete the contracts from our loyal subscribers.\n"
				+ "You will send out a group of three to take care of whatever needs to be done.\n"
				+ "This includes acquiring objects of interest for clients,\n"
				+ "Controlling the city and its civilians,\n"
				+ "And ‘taking care’ of troublesome individuals.\n"
				+ "Any questions?\n";
	}
	
	public static String toJob()
	{
		return "This is one of your three subordinates that you will coordinate.\n"
				+ "Like you, they are being trained for a full-time job.\n"
				+ "For now, expect small-scale contracts.\n"
				+ "I leave it up to you to learn along the way.\n";
	}
	
	
	
}
